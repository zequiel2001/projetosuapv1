    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // TODO add your handling code here:
        
        
        String nome = this.nome.getText();
        String cpf = this.cpf.getText();
        String endereco = this.ender.getText();
        String nasc = this.nasc.getText();
        
        Aluno aluno = new Aluno();
        
        aluno.setNome(nome);
        aluno.setCpf(cpf);
        aluno.setEndereco(endereco);
        aluno.setNascimento(nasc);
        
        AlunoBO bo = new AlunoBO();
        
        
        try {
            bo.cadastrar(aluno);
            JOptionPane.showMessageDialog(null, "Aluno cadastrada com sucesso");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Houve um erro na hora de cadastrar um aluno");
        }
        preencherTabela();
    }//GEN-LAST:event_btn1ActionPerformed

