    public void preencherTabela(){
        DefaultTableModel modeltable = (DefaultTableModel) tabelaAluno.getModel();
        
        modeltable.setNumRows(0);
        
        AlunoBO alunobo = new AlunoBO();
        
        for(Aluno aluno : alunobo.consulta()){
            modeltable.addRow(new Object[] {
                aluno.getNome(), aluno.getCpf(), aluno.getEndereco(), aluno.getNascimento()
            
            });
            
        
        }   
    }

